let myArr = [1, 2, 3, 4];

// pop function -> mengambil isi sebuah array dari belakang/akhir
myArr.pop();
console.log(myArr);

// push function -> menambah isi sebuah array dari belakang/akhir
myArr.push(5);
console.log(myArr);

// unshift function -> menambah isi sebuah array dari depan/awal
myArr.unshift(0);
console.log(myArr);

// shift function -> mengambil isi sebuah array dari depan/awal
myArr.shift();
console.log(myArr);
